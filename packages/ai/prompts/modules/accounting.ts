export function getAccountingKnowledge(locale: string): string {
  return `
## النظام المحاسبي
المسار: /[org]/finance

### دليل الحسابات (/finance/chart-of-accounts)
- شجرة حسابات هرمية تُنشأ تلقائياً (48 حساب افتراضي)
- 5 أنواع: أصول، خصوم، حقوق ملكية، إيرادات، مصروفات
- 4 مستويات (رئيسي → فرعي → تفصيلي → تحليلي)
- دفتر أستاذ لكل حساب مع رصيد تراكمي

### القيود اليومية (/finance/journal-entries)
- قيود تلقائية (14 حدث مالي) + قيود يدوية
- حالات: مسودة → مرحّل → معكوس
- ترحيل فردي وجماعي
- ترقيم تلقائي حسب النوع (INV-JE, EXP-JE, إلخ)
- بحث متقدم بالمبلغ والحساب والنوع والتاريخ

### الأرصدة الافتتاحية (/finance/opening-balances)
- إدخال أرصدة بداية الفترة عند أول استخدام للنظام

### الفترات المحاسبية (/finance/accounting-periods)
- شهرية، ربع سنوية، سنوية
- إغلاق الفترة يمنع الترحيل فيها

### التقارير المحاسبية (/finance/accounting-reports)
- ميزان المراجعة
- الميزانية العمومية
- قائمة الدخل
- مراكز التكلفة (ربحية كل مشروع)
- تقرير ضريبة القيمة المضافة
- أعمار الذمم المدينة والدائنة
- تصدير Excel لكل التقارير

### التسوية البنكية (/finance/banks/[id]/reconciliation)
- مطابقة يدوية مع كشف البنك

### القيود المتكررة (/finance/journal-entries/recurring)
- قوالب شهرية/ربعية/سنوية تولّد مسودات تلقائياً

### سندات القبض والصرف
- سند قبض: مستند مطبوع للمقبوضات
- سند صرف: مستند مطبوع للمصروفات
`;
}
